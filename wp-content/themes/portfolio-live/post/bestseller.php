    <div class="section-container col-sm-12">
        <img src="https://www.andreviallon.com/wp-content/uploads/2018/09/Bestseller-Portfolio-1024x513.png" alt="Bestseller-logo">
    </div>

    <div class="section-container col-sm-12 col-md-10 col-md-offset-1">
        <h2 class="post-section-title">Process</h2>
        <p class="section-content">The first step we took in this project was to do an in-depth analysis of the Bestseller brand as well as studying the material they provided us such as their target group and design guide. Thanks to the information collected we were able to create a persona to have a represent the typical user that will use our app.

    </div>

    <div class="section-container col-sm-6 col-sm-offset-3">
        <img src="https://www.andreviallon.com/wp-content/uploads/2018/07/bestseller-persona.png" alt="Bestseller-persona">
    </div>

    <div class="section-container col-sm-12 col-md-10 col-md-offset-1">
        <h2 class="post-section-title">User journeys</h2>
        <p class="section-content">The next step, once we had the persona done, was to map out the flow of the app. To do so we create user journeys taking into consideration the different goals that the users want to achieve as well as the different paths they may take to get there. Thanks to this we had an idea about the information that needs to be displayed to the user and the layout of the app.

    </div>
