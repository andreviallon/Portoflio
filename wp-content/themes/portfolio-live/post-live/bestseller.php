        <p class="section-content job">UI / UX Design
    </div>

    <div class="section-container title-container col-md-8 col-md-offset-2">
        <h2 class="post-section-title">overview</h2>
        <p class="section-content"> The aim of this project was to create a portal for Bestseller's employees, for them to have a better way of handling
            internal services such as orderings, bookings, requests, reporting incidents, and finding specific documents.
            To help the users fulfilling these goals, along with two other teammates, I created a prototype for a mobile application.
            <br>
            <br>
            <a href="https://xd.adobe.com/view/56fa443c-71c8-4bc4-5fa5-a9b857bc7400-20cb/" target="_blank" class="menu-hover">Click here to see the Adobe xd prototype.</a>
        </p>
    </div>

    <div class="section-container col-sm-12 col-sm-offset-0 col-md-10 col-md-offset-1">
        <img src="https://www.andreviallon.com/wp-content/uploads/2018/09/Bestseller-Portfolio-1024x513.png" alt="Bestseller-logo">
    </div>

    <div class="section-container title-container col-md-8 col-md-offset-2">
        <h2 class="post-section-title">process</h2>
        <p class="section-content">The first step we took in this project was to do an in-depth analysis of the Bestseller brand as well as studying
            the material they provided us such as their target group and design guide. Thanks to the information collected
            we were able to create a persona to have a represent the typical user that will use our app.</p>
    </div>

    <div class="section-container col-sm-12 col-sm-offset-0 col-md-6 col-md-offset-3">
        <img src="https://www.andreviallon.com/wp-content/uploads/2018/07/bestseller-persona.png" alt="Bestseller-persona">
    </div>


    <div class="section-container title-container col-md-8 col-md-offset-2">
        <h2 class="post-section-title">user journeys</h2>
        <p class="section-content">The next step, once we had the persona done, was to map out the flow of the app. To do so we create user journeys
            taking into consideration the different goals that the users want to achieve as well as the different paths they may take to get there. Thanks to this we had an idea about the information that needs to be displayed to the user and the layout of the app. </p>
    </div>
    
