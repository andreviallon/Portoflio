    <div class="section-container title-container col-md-8 col-md-offset-2">
        <h2 class="post-section-title">insight</h2>
        <p class="section-content">To improve the usability on the mobile version, I focused on positioning every element so that they can be easily
            accessible for the user using the ‘Thumb Zone’. For instance, all elements the user might click on the login page require a small or no stretch for the user’s thumb. Also, the menu button is placed on the top left corner
            rather than the right one because it is going to be used more frequently by the user rather than the notification
            button and so on.</p>
    </div>


