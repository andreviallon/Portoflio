    <div class="section-container title-container col-md-8 col-md-offset-2">
        <h2 class="post-section-title">overview</h2>
        <p class="section-content">Epico is a Danish IT consultancy company helping freelancers finding jobs and meeting recruiters. Epico wanted to
            create a web-app allowing freelancers to see available jobs as well as blog post that Epico are posting. The
            web-app should be responsive, user-friendly and facilitate the process of finding a job for freelancers.
            <br>
            <br>See all mockups on InVision at
            <a href="https://invis.io/WCETXJA6Z" target="_blank" class="menu-hover">https://invis.io/WCETXJA6Z</a>.</p>
    </div>

    <div class="section-container col-sm-12 col-sm-offset-0 col-md-10 col-md-offset-1">
        <img src="https://www.andreviallon.com/wp-content/uploads/2018/07/epico-1-1024x513.png" alt="Epico-1">
    </div>

    <div class="section-container title-container col-md-8 col-md-offset-2">
        <h2 class="post-section-title">process</h2>
        <p class="section-content">To create this product, I first started by analysis Epico’s website to understand their values and what they which
            to achieve with this web-app. Next, I listed all the different features that should be present asked by Epico such as the user being able to login via LinkedIn and so on. Then, I created user journeys to understand the flow and the direction the app should take. Finally, I started to do sketches and wireframes resulting in the
            mockups showcased.</p>
    </div>

    <div class="section-container col-sm-12 col-sm-offset-0 col-md-10 col-md-offset-1">
