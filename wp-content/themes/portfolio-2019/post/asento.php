        <p class="section-content job">UX Design
    </div>

    <div class="section-container col-sm-12 col-md-10 col-md-offset-1">
        <h2 class="post-section-title">Objective</h2>
        <p class="section-content">Design a product to help Marketing Coordinators learn and acquire new skills.</p>

        <a href="https://xd.adobe.com/view/56fa443c-71c8-4bc4-5fa5-a9b857bc7400-20cb/" target="_blank" class="menu-hover">Click here to see the prototype.</a>

    </div>

    <div class="section-container col-sm-12 col-md-10 col-md-offset-1">
        <h2 class="post-section-title">The Problem</h2>
        <p class="section-content">Asento is a young and ambitious marketing company based in Kolding, Denmark and have for aim to disrupt the danish e-learning industry. Asento has a YouTube channel where they upload courses with very long videos, often over an hour long. This makes it hard for their followers to keep up with all the content and information in each video.</p>
    </div>

    <div class="section-container col-sm-12">
        <img src="https://www.andreviallon.com/wp-content/uploads/2018/08/Asento-thumbnail-1024x513.png" alt="Asento thumbnail">
    </div>

    <div class="section-container col-sm-12 col-md-10 col-md-offset-1">
        <h2 class="post-section-title">The Solution</h2>
        <p class="section-content">Create easy to use and intuitive an e-learning platform to educate marketing coordinators, focusing on a desktop web-app. Because Marketing Coordinators already have busy schedule, the solution should have small videos grouped in courses so that the can easily find the information they need without having to what the entire course.Moreover, the solution should include certificates that the users can add to their linkedin account to showcase their skills.</p>
    </div>

    <div class="section-container col-sm-12 col-md-10 col-md-offset-1">
        <h2 class="post-section-title">Planning and research phase</h2>
        <p class="section-content">After defining the problem and identifying the solution, the next step was to design planning and research. In this phase, I research the competition, build a persona, sketch out ideas and build wireframes as well as user journeys that solves for user requirements.</p>
    </div>

    <div class="section-container col-sm-12 col-md-10 col-md-offset-1">
        <img src="https://www.andreviallon.com/wp-content/uploads/2018/08/Asento-Persona.png" alt="Asento Persona">
    </div>

    <div class="section-container col-sm-12 col-md-10 col-md-offset-1">
        <h2 class="post-section-title">User Journey</h2>
    </div>
    <div class="section-container col-sm-12 col-md-10 col-md-offset-1">
        <ul class="section-content ul-section-content">
            <h4>Context</h4>
            <li>Line works in a medium size company as a Marketing Coordinator</li>
            <li>The company buys a SEO package (short term solution) from Asento</li>
            <li>Line needs to study SEO as she will have to take over Asento’s work in the future and work on the company’s</li>
            <li>website’s SEO.</li>
        </ul>

        <ol>
            <li>Line gets an email invitation from Asento to join the E-Learning Platform (email contains description of the platform and what the email is about + link to the website + login and temporary password) Line clicks on the link (taken to the login page directly) and inputs the information that was given in the email</li>
            <li>Line is asked to give a new password different from the one in the email</li>
            <li>Line is redirected to the Home Page</li>
            <li>Line is shown the 3 main categories (topics) that Asento offers and clicks on SEO</li>
            <li>She is now presented a list of SEO related courses (option to filter them and sort them by certain criteria)</li>
            <li>Line chooses a course and watches it</li>
            <li>When the When she finished watching she is shown a suggestion of courses related to the one she just watched</li>
            <li>Line does not have time to watch another course but would like to watch it later</li>
            <li>She clicks on “add to bookmarks” button to save it for later</li>
            <li>A few days later Line has time to watch the course she saved for later</li>
            <li>When reaching the home page Line is shown the courses that she bookmarked, that course that she started and did not finish so that she can pick up from where she left of and recommendation of courses that she may find interesting based on the previous courses that she watched</li>
        </ol>
    </div>

